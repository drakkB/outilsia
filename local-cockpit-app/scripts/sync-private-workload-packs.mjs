#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(appRoot, "..");
const sourcePath = join(repoRoot, "server-work", "static", "data", "private-workload-packs.json");
const generatedPath = join(appRoot, "src", "private-workload-packs.js");
const checkOnly = process.argv.includes("--check");

function fail(message) {
  console.error(`private_workload_pack_catalog_error ${message}`);
  process.exit(1);
}

if (!existsSync(sourcePath)) fail(`missing ${sourcePath}`);
let catalog;
try {
  catalog = JSON.parse(readFileSync(sourcePath, "utf8"));
} catch (error) {
  fail(`invalid JSON: ${error.message}`);
}

if (catalog.schema !== "outilsia.private_workload_pack_catalog.v1") fail("unexpected schema");
if (!/^\d{4}-\d{2}-\d{2}\.\d+$/.test(String(catalog.version || ""))) fail("version must be YYYY-MM-DD.N");
if (!/^\d{4}-\d{2}-\d{2}$/.test(String(catalog.updated_at || ""))) fail("updated_at must be YYYY-MM-DD");
for (const [key, expected] of Object.entries({
  local_only: true,
  cloud_upload: false,
  installed_models_only: true,
  downloads_per_run: 0,
  deterministic_checks_only: true,
  local_judgment_optional: false,
  persist_raw_custom_prompt: false,
  persist_raw_model_output: false,
  include_raw_content_in_passport: false
})) {
  if (catalog.policy?.[key] !== expected) fail(`policy ${key} must be ${JSON.stringify(expected)}`);
}
if (catalog.policy?.min_models_per_run !== 2 || catalog.policy?.max_models_per_run !== 3) {
  fail("model budget must remain 2..3");
}
if (!Array.isArray(catalog.response_keys) || catalog.response_keys.join(",") !== "answer,evidence,action") {
  fail("response_keys must be answer,evidence,action");
}
const packs = Array.isArray(catalog.packs) ? catalog.packs : [];
const expectedPacks = ["code", "french", "summary", "memory", "custom"];
if (packs.map((pack) => pack.key).join(",") !== expectedPacks.join(",")) fail("pack order or keys changed");
for (const pack of packs) {
  if (!pack.label || !pack.description) fail(`pack metadata missing: ${pack.key}`);
  if (pack.key === "custom" ? !pack.prompt_template : !pack.prompt) fail(`pack prompt missing: ${pack.key}`);
  if (!Array.isArray(pack.checks) || !pack.checks.length) fail(`checks missing: ${pack.key}`);
  const total = pack.checks.reduce((sum, check) => sum + Number(check.weight || 0), 0);
  if (total !== 100) fail(`check weights must total 100: ${pack.key}=${total}`);
  const keys = new Set();
  for (const check of pack.checks) {
    if (!check.key || keys.has(check.key)) fail(`duplicate or missing check key: ${pack.key}`);
    keys.add(check.key);
    if (!["exact_json_keys", "regex", "includes_all", "includes_any", "dynamic_required_terms"].includes(check.rule)) {
      fail(`unsupported rule ${check.rule}: ${pack.key}`);
    }
  }
}

const generated = [
  "// Generated by scripts/sync-private-workload-packs.mjs. Do not edit manually.",
  `globalThis.__OUTILSIA_PRIVATE_WORKLOAD_PACKS__ = Object.freeze(${JSON.stringify(catalog, null, 2)});`,
  ""
].join("\n");

if (checkOnly) {
  if (!existsSync(generatedPath)) fail(`generated asset missing: ${generatedPath}`);
  if (readFileSync(generatedPath, "utf8") !== generated) {
    fail("generated asset is stale; run npm run sync:private-workload-packs");
  }
  console.log(`private_workload_pack_catalog_ok version=${catalog.version} packs=${packs.length}`);
  process.exit(0);
}

writeFileSync(generatedPath, generated);
console.log(`private_workload_pack_catalog_synced version=${catalog.version} output=${generatedPath}`);

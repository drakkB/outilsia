#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(appRoot, "..");
const sourcePath = join(repoRoot, "server-work", "static", "data", "runtime-driver-matrix.json");
const generatedPath = join(appRoot, "src", "runtime-driver-matrix.js");
const checkOnly = process.argv.includes("--check");

function fail(message) {
  console.error(`runtime_driver_matrix_error ${message}`);
  process.exit(1);
}

if (!existsSync(sourcePath)) fail(`missing ${sourcePath}`);
let matrix;
try {
  matrix = JSON.parse(readFileSync(sourcePath, "utf8"));
} catch (error) {
  fail(`invalid JSON: ${error.message}`);
}

if (matrix.schema !== "outilsia.runtime_driver_matrix.v1") fail("unexpected schema");
if (!/^\d{4}-\d{2}-\d{2}\.\d+$/.test(String(matrix.version || ""))) fail("version must be YYYY-MM-DD.N");
for (const key of ["updated_at", "review_after"]) {
  if (!/^\d{4}-\d{2}-\d{2}$/.test(String(matrix[key] || ""))) fail(`${key} must be YYYY-MM-DD`);
}
if (new Date(`${matrix.review_after}T00:00:00Z`) <= new Date(`${matrix.updated_at}T00:00:00Z`)) {
  fail("review_after must be later than updated_at");
}
for (const sourceId of [
  "ollama_gpu",
  "nvidia_cuda_compat",
  "nvidia_architecture_matrix",
  "nvidia_driver",
  "amd_rocm_windows",
  "amd_driver",
  "intel_driver",
  "intel_oem_warning",
  "microsoft_wsl_gpu"
]) {
  const source = matrix.sources?.[sourceId];
  if (!source || !/^https:\/\//.test(String(source.url || ""))) fail(`official source missing: ${sourceId}`);
  if (!/^\d{4}-\d{2}-\d{2}$/.test(String(source.checked_at || ""))) fail(`checked_at missing: ${sourceId}`);
}
for (const backend of ["cuda", "rocm", "vulkan", "metal", "directml", "cpu"]) {
  if (!matrix.backends?.[backend]) fail(`backend missing: ${backend}`);
}
for (const vendor of ["nvidia", "amd", "intel", "apple", "cpu"]) {
  if (!matrix.vendors?.[vendor]) fail(`vendor missing: ${vendor}`);
}
if (matrix.policy?.reported_api_is_not_runtime_proof !== true) fail("reported API must not count as runtime proof");
if (matrix.policy?.shared_memory_is_not_dedicated_vram !== true) fail("shared memory policy missing");
if (matrix.policy?.driver_installation?.automatic_install_supported !== false) fail("automatic driver install must remain disabled in v1");
if (matrix.policy?.driver_installation?.silent_elevation_forbidden !== true) fail("silent elevation must be forbidden");

const generated = [
  "// Generated by scripts/sync-runtime-driver-matrix.mjs. Do not edit manually.",
  `globalThis.__OUTILSIA_RUNTIME_DRIVER_MATRIX__ = Object.freeze(${JSON.stringify(matrix, null, 2)});`,
  ""
].join("\n");

if (checkOnly) {
  if (!existsSync(generatedPath)) fail(`generated asset missing: ${generatedPath}`);
  if (readFileSync(generatedPath, "utf8") !== generated) fail("generated asset is stale; run npm run sync:runtime-driver-matrix");
  console.log(`runtime_driver_matrix_ok version=${matrix.version} sources=${Object.keys(matrix.sources).length} backends=${Object.keys(matrix.backends).length}`);
  process.exit(0);
}

writeFileSync(generatedPath, generated);
console.log(`runtime_driver_matrix_synced version=${matrix.version} output=${generatedPath}`);
